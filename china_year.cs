using System; // Подключаем пространство имен System для работы с консолью

// Объявляем класс Program с модификатором partial, 
// чтобы избежать конфликта с автоматически созданным классом в .NET 6+
partial class Program
{
    // Главный метод, точка входа в программу
    static void Main()
    {
        // Просим пользователя ввести год рождения
        Console.Write("Введите год рождения: ");

        int year; // Переменная для хранения введенного года

        // Проверяем корректность ввода: ввод должен быть целым числом
        while (!int.TryParse(Console.ReadLine(), out year))
        {
            Console.Write("Некорректный ввод! Введите целое число: ");
        }

        // Создаем массив с названиями животных по порядку китайского гороскопа
        string[] zodiacAnimals = {
            "Обезьяна", "Петух", "Собака", "Свинья",
            "Крыса", "Бык", "Тигр", "Кролик",
            "Дракон", "Змея", "Лошадь", "Коза"
        };

        // Вычисляем индекс животного в массиве
        // 2004 - это год Крысы, берем его как отправную точку
        int index = (year - 2004) % 12;

        // Если индекс получился отрицательным (например, если год меньше 2004),
        // добавляем 12, чтобы получить корректный индекс
        if (index < 0) index += 12;

        // Выводим результат пользователю
        Console.WriteLine($"Ваш знак китайского гороскопа: {zodiacAnimals[index]}");
    }
}